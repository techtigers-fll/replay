class Color:
    """Enumeration of supported "stop on" colors
    """

    """White color
    """
    WHITE = "white"

    """Black color
    """
    BLACK = "black"

    """Violet color
    """
    VIOLET = "violet"

    """Blue color
    """
    BLUE = "blue"

    """Cyan color
    """
    CYAN = "cyan"

    """Yellow color
    """
    YELLOW= "yellow"

    """Red color
    """
    RED= "red"
