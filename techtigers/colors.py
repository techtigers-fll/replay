class Color:
    """Enumeration of supported "stop on" colors
    """

    """White color
    """
    WHITE = range(90, 101)

    """Black color
    """
    BLACK = range(0, 41)

    """Violet color
    """
    VIOLET = range(0, 21)

    """Blue color
    """
    BLUE = range(0, 21)

    """Cyan color
    """
    CYAN = range(0, 21)

    """Yellow color
    """
    YELLOW= range(0, 21)

    """Red color
    """
    RED= range(0, 21)

    """Green color
    """
    GREEN = range(80, 91)
